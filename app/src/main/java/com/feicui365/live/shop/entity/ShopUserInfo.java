package com.feicui365.live.shop.entity;

public class ShopUserInfo {
    int isShop;
    int is_anchor;
    int undelivery_count;
    int unevaluate_count;
    int unpay_count;
    int unreceive_count;

    public ShopUserInfo() {
    }

    public int getIsShop() {
        return isShop;
    }

    public void setIsShop(int isShop) {
        this.isShop = isShop;
    }

    public int getIs_anchor() {
        return is_anchor;
    }

    public void setIs_anchor(int is_anchor) {
        this.is_anchor = is_anchor;
    }

    public int getUndelivery_count() {
        return undelivery_count;
    }

    public void setUndelivery_count(int undelivery_count) {
        this.undelivery_count = undelivery_count;
    }

    public int getUnevaluate_count() {
        return unevaluate_count;
    }

    public void setUnevaluate_count(int unevaluate_count) {
        this.unevaluate_count = unevaluate_count;
    }

    public int getUnpay_count() {
        return unpay_count;
    }

    public void setUnpay_count(int unpay_count) {
        this.unpay_count = unpay_count;
    }

    public int getUnreceive_count() {
        return unreceive_count;
    }

    public void setUnreceive_count(int unreceive_count) {
        this.unreceive_count = unreceive_count;
    }
}
